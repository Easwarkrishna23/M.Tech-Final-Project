def fig_graph_comparison(cora, model, params, save_path):
    """Graph structure comparison for two representative attacks."""
    print("\n[Fig 4] Graph comparison (Nettack) …")

    attacked_path = ROOT / "results" / "attacked_graphs" / "cora_nettack.npz"
    defended_path = ROOT / "results" / "defended_graphs" / "defended_nettack.npz"

    if not attacked_path.exists() or not defended_path.exists():
        print("  [Skip] attacked/defended graphs not found")
        return

    g_attacked = load_graph_npz(attacked_path, cora)
    g_defended = load_graph_npz(defended_path, cora)

    # Use test nodes near the graph center as "center_nodes"
    test_nodes = np.where(cora.test_mask)[0][:5].tolist()

    plot_graph_comparison(
        adj_clean=cora.adj,
        adj_attacked=g_attacked.adj,
        adj_defended=g_defended.adj,
        labels=cora.labels,
        center_nodes=test_nodes,
        attack_name="nettack",
        dataset_name="Cora",
        hops=2,
        save_path=save_path,
    )

    print("\n[Fig 4b] Graph comparison (Feature Perturbation) …")
    attacked_path_fp = ROOT / "results" / "attacked_graphs" / "cora_feature_perturbation.npz"
    defended_path_fp = ROOT / "results" / "defended_graphs" / "defended_feature_perturbation.npz"
    if attacked_path_fp.exists() and defended_path_fp.exists():
        g_atk_fp = load_graph_npz(attacked_path_fp, cora)
        g_def_fp = load_graph_npz(defended_path_fp, cora)
        plot_graph_comparison(
            adj_clean=cora.adj,
            adj_attacked=g_atk_fp.adj,
            adj_defended=g_def_fp.adj,
            labels=cora.labels,
            center_nodes=test_nodes,
            attack_name="feature_perturbation",
            dataset_name="Cora",
            hops=2,
            save_path=save_path,
        )


def fig_degree_distributions(cora, save_path):
    print("\n[Fig 5] Degree distributions …")
    for attack_name in ["nettack", "feature_perturbation", "gradient_attack"]:
        atk_path = ROOT / "results" / "attacked_graphs" / f"cora_{attack_name}.npz"
        def_path = ROOT / "results" / "defended_graphs" / f"defended_{attack_name}.npz"
        if not atk_path.exists():
            continue
        g_atk = load_graph_npz(atk_path, cora)
        g_def = load_graph_npz(def_path, cora) if def_path.exists() else g_atk
        plot_degree_distribution(
            adj_clean=cora.adj,
            adj_attacked=g_atk.adj,
            adj_defended=g_def.adj,
            attack_name=attack_name,
            dataset_name="Cora",
            save_path=save_path,
        )


def fig_embeddings(cora, model, params, save_path):
    """t-SNE of GCN hidden embeddings for clean / attacked / defended."""
    print("\n[Fig 6] t-SNE embeddings …")

    # Clean embeddings from saved baseline params
    emb_clean, _, _ = predict(model, params, cora)
    labels_np = np.array(cora.labels)

    for attack_name in ["gradient_attack", "feature_perturbation", "nettack"]:
        atk_path = ROOT / "results" / "attacked_graphs" / f"cora_{attack_name}.npz"
        def_path = ROOT / "results" / "defended_graphs" / f"defended_{attack_name}.npz"
        if not atk_path.exists():
            continue

        g_atk = load_graph_npz(atk_path, cora)
        g_def = load_graph_npz(def_path, cora) if def_path.exists() else g_atk

        emb_atk, _, _ = predict(model, params, g_atk)
        emb_def, _, _ = predict(model, params, g_def)

        # Only use nodes with valid labels
        valid = labels_np >= 0
        plot_embeddings_comparison(
            emb_clean=np.array(emb_clean)[valid],
            emb_attacked=np.array(emb_atk)[valid],
            emb_defended=np.array(emb_def)[valid],
            labels=labels_np[valid],
            attack_name=attack_name,
            dataset_name="Cora",
            method="tsne",
            max_nodes=1500,
            save_path=save_path,
        )


def fig_training_curves(cora, model, save_path):
    """Re-run a short training to capture loss/val curves."""
    print("\n[Fig 7] Training curves (GCN on Cora) …")
    from models.train import train_model
    result = train_model(model, cora, cfg.model, seed=cfg.seed, verbose=False)
    plot_training_curves(
        train_losses=result.train_losses,
        val_accs=result.val_accs,
        model_name="GCN",
        dataset_name="Cora",
        save_path=save_path,
    )


