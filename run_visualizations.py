def load_graph_npz(path: Path, ref_graph: GraphData) -> GraphData:
    """Load a saved graph npz and re-wrap as GraphData."""
    d = np.load(path)
    g = ref_graph.copy()
    g = g.update_adj(d["adj"])
    g = g.update_features(d["features"])
    return g


def load_cora_graph():
    from datasets.cora_loader import load_cora
    print("[Data] Loading Cora …")
    return load_cora(cfg.data_dir)


def load_gcn_and_params(cora):
    import jax
    import jax.numpy as jnp
    from utils.graph_utils import normalize_adjacency

    model = create_gcn(
        hidden_dim=cfg.model.hidden_dim,
        num_classes=cora.num_classes,
        dropout_rate=cfg.model.dropout_rate,
    )
    # Init dummy params for tree structure, then overwrite with saved values
    a_hat = jnp.array(normalize_adjacency(cora.adj))
    x     = jnp.array(cora.features)
    key   = jax.random.PRNGKey(0)
    template_params = model.init({"params": key, "dropout": key}, x, a_hat, training=False)["params"]

    params = load_params(template_params, str(cfg.checkpoints_dir / "gcn_cora_baseline"))
    return model, params


